    private void cadastrar(){
        Pessoa p = new Pessoa();
        ArrayList<String> opt = new ArrayList<>();
        
        p.setNome(jtNome.getText());
        
        if(jcPS4.isSelected()){
            opt.add(jcPS4.getText());
        }else{
            opt.add("-");
        }
        
        if(jcXBOX.isSelected()){
            opt.add(jcXBOX.getText());
        }else{
            opt.add("-");
        }
        
        p.setConsole(opt);

        //JOptionPane.showMessageDialog(rootPane, "Dados"+p.toString(), "Relat√≥rio", JOptionPane.PLAIN_MESSAGE);
        
        //jtResposta.setText("Dados: "+p.toString());
        jtResposta.setText(jtResposta.getText()+"\n\n####"+p.toString());
    }//fecha cadastrar
    
    private void limpar(){
        jtNome.setText(null);
        
        jcPS4.setSelected(false);
        jcXBOX.setSelected(false);
    }//fecha limpar
    
    private void jbCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCadastrarActionPerformed
        cadastrar();
        limpar();
    }//GEN-LAST:event_jbCadastrarActionPerformed

